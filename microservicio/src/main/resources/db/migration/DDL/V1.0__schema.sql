-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `DEVOLUCION_VEHICULOS` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `fecha_entrega` DATE NOT NULL,
  `porcentaje_de_dano` INT NOT NULL,
  `valor_por_mora` DOUBLE NOT NULL,
  `valor_por_danos` DOUBLE NOT NULL,
  `valor_total_a_pagar` DOUBLE NOT NULL,
  `ALQUILER_VEHICULOS_id` INT NOT NULL,
  `ALQUILER_VEHICULOS_USUARIOS_id` INT NOT NULL,
  `ALQUILER_VEHICULOS_VEHICULOS_id` INT NOT NULL,
  `ALQUILER_VEHICULOS_VEHICULOS_placa` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`id`, `ALQUILER_VEHICULOS_id`, `ALQUILER_VEHICULOS_USUARIOS_id`, `ALQUILER_VEHICULOS_VEHICULOS_id`, `ALQUILER_VEHICULOS_VEHICULOS_placa`),
  INDEX `fk_DEVOLUCION_VEHICULOS_ALQUILER_VEHICULOS1_idx` (`ALQUILER_VEHICULOS_id` ASC, `ALQUILER_VEHICULOS_USUARIOS_id` ASC, `ALQUILER_VEHICULOS_VEHICULOS_id` ASC, `ALQUILER_VEHICULOS_VEHICULOS_placa` ASC) VISIBLE,
  CONSTRAINT `fk_DEVOLUCION_VEHICULOS_ALQUILER_VEHICULOS1`
    FOREIGN KEY (`ALQUILER_VEHICULOS_id` , `ALQUILER_VEHICULOS_USUARIOS_id` , `ALQUILER_VEHICULOS_VEHICULOS_id`)
    REFERENCES `ALQUILER_VEHICULOS` (`id` , `USUARIOS_id` , `VEHICULOS_id`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB;



